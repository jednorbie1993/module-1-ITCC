#include <stdio.h>  
// stdio.h = standard input output header
// kailangan ito para gumana ang printf()

int main() {  
    // main() = dito nagsisimula tumakbo ang program

    printf("Hello, Jed!\n");  
    // printf = nagpapakita ng text sa screen
    // \n = new line (parang pindot ng Enter)

    return 0;  
    // ibig sabihin: successful na natapos ang program
}

/*
SUMMARY (para sa future self mo):

- .c file = source code (dito ka nagta-type)
- #include <stdio.h> = para magamit ang printf
- main() = starting point ng program
- printf() = output sa screen
- return 0 = okay ang takbo ng program
*/
